DROP TABLE IF EXISTS CURRICULAR_RULE;
CREATE TABLE CURRICULAR_RULE (
	ID_INTERNAL int(11) NOT NULL auto_increment,
	CLASS_NAME varchar(255) NOT NULL,	
	KEY_DEGREE_MODULE_TO_APPLY_RULE int(11) NOT NULL,	
	KEY_BEGIN_EXECUTION_PERIOD int(11) NOT NULL,	
	KEY_END_EXECUTION_PERIOD int(11) default NULL,	
	KEY_PARENT_COMPOSITE_RULE int(11) default NULL,	
	CURRICULAR_RULE_TYPE varchar(100) NOT NULL,
	COMPOSITE_RULE_TYPE varchar(100) default NULL,
	PRIMARY KEY  (ID_INTERNAL)
) TYPE=InnoDB;
