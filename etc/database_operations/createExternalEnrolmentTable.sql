CREATE TABLE EXTERNAL_ENROLMENT(
  ID_INTERNAL int(11) unsigned NOT NULL auto_increment,
  KEY_ROOT_DOMAIN_OBJECT int(11) NOT NULL default '1',
  KEY_STUDENT int(11) NOT NULL,
  KEY_EXECUTION_PERIOD int(11) NOT NULL,
  KEY_EXTERNAL_CURRICULAR_COURSE int(11) NOT NULL,
  GRADE varchar(50) NOT NULL,
  CREATED_BY varchar(50) NOT NULL,
  CREATION_DATE_DATE_TIME datetime NOT NULL,
  PRIMARY KEY  (ID_INTERNAL),
  KEY KEY_ROOT_DOMAIN_OBJECT (KEY_ROOT_DOMAIN_OBJECT),
  KEY KEY_STUDENT (KEY_STUDENT),
  KEY KEY_EXECUTION_PERIOD (KEY_EXECUTION_PERIOD),
  KEY KEY_EXTERNAL_CURRICULAR_COURSE (KEY_EXTERNAL_CURRICULAR_COURSE)
) ENGINE=InnoDB;
