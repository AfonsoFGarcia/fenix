UPDATE PARTY_CONTACT AS PC, PARTY AS P
SET PC.VISIBLE = P.AVAILABLE_WEB_SITE
WHERE PC.KEY_PARTY = P.ID_INTERNAL AND PC.OJB_CONCRETE_CLASS like "%WebAddress";

UPDATE PARTY_CONTACT AS PC, PARTY AS P
SET PC.VISIBLE = P.AVAILABLE_EMAIL
WHERE PC.KEY_PARTY = P.ID_INTERNAL AND PC.OJB_CONCRETE_CLASS like "%EmailAddress";

UPDATE PARTY_CONTACT
SET VISIBLE = 0
WHERE OJB_CONCRETE_CLASS like "%Phone" OR OJB_CONCRETE_CLASS like "%MobilePhone";
