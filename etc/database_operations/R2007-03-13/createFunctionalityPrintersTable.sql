
CREATE TABLE FUNCTIONALITY_PRINTERS (
  ID_INTERNAL int(11) NOT NULL auto_increment,
  KEY_ROOT_DOMAIN_OBJECT int(11) NOT NULL default '1',
  FUNCTIONALITY varchar(255) not null,
  KEY_PRINTERS varchar(255) not null,
  PRIMARY KEY (ID_INTERNAL),
  KEY KEY_ROOT_DOMAIN_OBJECT (KEY_ROOT_DOMAIN_OBJECT)
);

CREATE TABLE UNIT_FUNCTIONALITY_PRINTERS (
  ID_INTERNAL int(11) NOT NULL auto_increment,
  KEY_UNIT int(11) NOT NULL,
  KEY_FUNCTIONALITY_PRINTERS int(11) NOT NULL,
  PRIMARY KEY (ID_INTERNAL),
  UNIQUE KEY KEY_UNIT_KEY_FUNCTIONALITY_PRINTERS (KEY_UNIT, KEY_FUNCTIONALITY_PRINTERS),
  KEY KEY_UNIT (KEY_UNIT),
  KEY KEY_FUNCTIONALITY_PRINTERS (KEY_FUNCTIONALITY_PRINTERS)
);

