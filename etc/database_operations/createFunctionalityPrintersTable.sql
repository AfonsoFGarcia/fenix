  UNIQUE KEY KEY_UNIT_KEY_FUNCTIONALITY_PRINTERS (KEY_UNIT, KEY_FUNCTIONALITY_PRINTERS),
  KEY KEY_UNIT (KEY_UNIT),
  KEY KEY_FUNCTIONALITY_PRINTERS (KEY_FUNCTIONALITY_PRINTERS)
