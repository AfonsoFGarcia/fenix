CREATE TABLE ENROLMENT_EVALUATION_LOG (
  `ID_INTERNAL` int(11) NOT NULL auto_increment,
  `OID` bigint(20),
  `OID_ROOT_DOMAIN_OBJECT` bigint(20),
  `ACTION` varchar(255),
  `WHO` varchar(255),
  `GRADE_VALUE` varchar(255),
  `GRADE_SCALE` varchar(255),
  `ENROLMENT_EVALUATION_TYPE` varchar(255),
  `ENROLMENT_EVALUATION_STATE` varchar(255),
  `EXECUTION_SEMESTER` varchar(255),
  `CURRICULAR_COURSE` varchar(255),
  `EXAM_DATE` varchar(255),
  `ENROLMENT_EVALUATION_RESPONSIBLE` varchar(255),
  `BOOK` varchar(255),
  `PAGE` varchar(255),
  `ACTION_DATE` varchar(255),
  primary key (ID_INTERNAL),
  index(OID)
) TYPE=InnoDB, character set latin1 ;
