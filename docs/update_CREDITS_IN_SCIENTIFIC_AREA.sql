SELECT CONCAT('UPDATE CREDITS_IN_SCIENTIFIC_AREA SET KEY_SCIENTIFIC_AREA = 16 WHERE ID_INTERNAL = ', C.ID_INTERNAL, ';') AS ""
FROM CREDITS_IN_SCIENTIFIC_AREA C
INNER JOIN ENROLMENT E ON C.KEY_ENROLMENT = E.ID_INTERNAL
INNER JOIN CURRICULAR_COURSE CC ON E.KEY_CURRICULAR_COURSE = CC.ID_INTERNAL
WHERE CC.CODE = 'HE';
