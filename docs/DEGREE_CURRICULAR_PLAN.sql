  corresponde à frequência de 12 disciplinas que não pertençam ao
  tronco comum e formem um todo coerente do ponto de vista
  científico-pedagógico. Uma área secundária corresponde à frequência
  de 3 disciplinas que não pertençam ao tronco comum e formem
  igualmente um todo coerente do ponto de vista
  cientifico-pedagógico. A escolha da área secundária é
  obrigatoriamente distinta da área de especialização e os respectivos
  créditos deverão ser obtidos em disciplinas distintas das que são
  frequentadas na respectiva área de especialização.</font></p>

<p><font face=""Helvetica, Arial"">A formação de base ocupa um pouco
  mais que os primeiros 5 semestres do curso. A partir do 6º semestre,
  o conceito de ""ano"" deixa de ser relevante num regime de
  créditos. Isto é, chegando ao 3º ano, 2º semestre, e optando por uma
  dada área de especialização, o aluno poderá escolher, entre um leque
  relativamente largo de disciplinas, as que tem que fazer para
  prefazer um dado número de créditos nessa área. A frequência de uma
  dada disciplina não está assim condicionada a um ""ano"", mas ao facto
  de o aluno ter obtido aprovação nas disciplinas indicadas como
  precedência dessa disciplina. </font></p>
