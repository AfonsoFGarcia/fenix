<p><font face=""Helvetica, Arial""><b>2.1. Área de Especialização:
  ""Computadores""</b></font></p>

<p><font face=""Helvetica, Arial"">A actualidade desta área de
  especialização é indiscutível. Através da escolha desta área, a
  sólida formação de base em engenharia electrotécnica adquirida
  durante os três primeiros anos do curso pode ser complementada com
  tópicos avançados de arquitectura de computadores, metodologias de
  programação, redes de comunicações, sistemas multimédia,
  aprendizagem automática e electrónica de sistemas
  computacionais. </font></p>

<p><font face=""Helvetica, Arial""><b>2.2. Área de Especialização:
""Energia""</b></font></p>

<p><font face=""Helvetica, Arial"">A produção, transporte, distribuição,
  processamento e utilização da energia eléctrica, continua a ser hoje
  uma das áreas mais relevantes da engenharia electrotécnica. Sem um
  fornecimento seguro, fiável e económico desta forma de energia, a
  sociedade moderna ficaria incapacitada.</font></p>

<p><font face=""Helvetica, Arial"">O Sistema de Energia
  Eléctrica-compreendendo as centrais eléctricas, as redes e as cargas
  - com dimensão à escala continental, resultante da interligação
  entre os sistemas nacionais - constitui um dos mais complexos
  sistemas jamais concebidos por cientistas e engenheiros. Esta área
  de especialização abrange a conversão electromecânica de energia, a
  produção convencional e descentralizada, o transporte e distribuição
  em (muito) alta, média e baixa tensão, a análise de redes e sistemas
  de energia, as máquinas eléctricas, a electrónica industrial e de
  energia, o controlo dos sistemas e dos accionamentos, a protecção e
  automação dos equipamentos e instalações, e a qualidade da energia
  eléctrica.</font></p>

<p><font face=""Helvetica, Arial"">A reestruturação que em todo o mundo
  se tem vindo a operar no sector eléctrico-tradicionalmente assente
  em monopólios naturais - visando a criação de um ambiente mais
  competitivo - bem como os progressos no uso da energia eléctrica -
  na indústria, nos serviços, nos transportes e nas residências -
  permite antever um crescimento sustentado das oportunidades
  profissionais para os licenciados que optem por esta área de
  especialização.  </font></p>

<p><font face=""Helvetica, Arial""><b>2.3. Área de Especialização: ""Sistemas, 
  Decisão e Controlo""</b></font></p>

<p><font face=""Helvetica, Arial"">Num sistema de decisão e controlo os
  dados recolhidos sobre um determinado processo (ângulos das juntas
  num braço robot, temperaturas numa caldeira, carros por hora num
  ponto da auto-estrada) são tratados por forma a serem tomadas
  decisões. Por exemplo, observando o trãnsito numa auto-estrada
  podemos querer decidir se ele segue um padrão normal ou se há
  variações devidas a um acidente. Isto implica o processamento por
  computador de imagens recolhidas por câmaras e ainda o
  reconhecimento automático de padrões do trânsito (anormalmente fraco
  ou forte, veículos muito juntos, etc.). Um outro exemplo é controlo
  da temperatura numa caldeira. Através de sucessivas medidas de
  temperatura, um computador pode efectuar cálculos para decidir se
  certas válvulas devem ser mais ou menos abertas por forma a que a
  temperatura se mantenha no valor desejado.</font></p>

<p><font face=""Helvetica, Arial"">O domínio das técnicas da Engenharia
  dos Sistemas de Decisão e Controlo é uma condição indispensável para
  a modernização da Indústria.  Para intervir, à escala nacional, num
  ambiente industrial caracterizado por uma sofisticação crescente, é
  fundamental preparar, com tempo, recursos humanos capazes de tornar
  essa intervenção eficaz em domínios tão diversos como as
  engenharias, a medicina, o controlo e gestão da produção, o
  ambiente, etc.. Os alunos desta área têm uma formação marcadamente
  interdisciplinar e são confrontados com aplicações que incluem:
  sistemas para controlo de processos e sistemas industriais;
  manipuladores robóticos e robots autónomos (para ambientes fabris ou
  em tarefas de prospecção, inspecção e manutenção em ambientes
  terrestres, submarinos ou aéreos); sistemas de automação; sistemas
  de visão artificial; biomedicina e bioengenharia; controlo em tempo
  real de sistemas complexos; etc. </font></p>

<p><font face=""Helvetica, Arial""><b>2.4. Área de Especialização:
""Sistemas Electrónicos""</b></font></p>

<p><font face=""Helvetica, Arial"">O objectivo da especialização em
  Sistemas Electrónicos é a formação de profissionais com qualificação
  superior dotados de capacidades para intervir, de forma inovadora,
  na introdução e acompanhamento das novas tecnologias de realização
