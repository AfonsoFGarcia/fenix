INNER JOIN STUDENT S1 ON SCP1.KEY_STUDENT = S1.ID_INTERNAL
INNER JOIN STUDENT S2 ON SCP2.KEY_STUDENT = S2.ID_INTERNAL
WHERE
S1.ID_INTERNAL = S2.ID_INTERNAL AND
SCP1.ID_INTERNAL = SCP2.ID_INTERNAL AND
CC1.NAME = CC2.NAME AND
EP1.ID_INTERNAL = EP2.ID_INTERNAL AND
EE1.ID_INTERNAL <> EE2.ID_INTERNAL AND
E1.ID_INTERNAL <> E2.ID_INTERNAL AND
EE1.GRADE IS NULL AND
EE2.GRADE IS NOT NULL AND
S1.DEGREE_TYPE = 1 AND
EP1.STATE <> 'C';

