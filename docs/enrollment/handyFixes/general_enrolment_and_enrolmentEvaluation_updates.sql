INNER JOIN EXECUTION_PERIOD EP2 ON E2.KEY_EXECUTION_PERIOD = EP2.ID_INTERNAL
INNER JOIN CURRICULAR_COURSE CC1 ON E1.KEY_CURRICULAR_COURSE = CC1.ID_INTERNAL
INNER JOIN CURRICULAR_COURSE CC2 ON E2.KEY_CURRICULAR_COURSE = CC2.ID_INTERNAL
INNER JOIN STUDENT_CURRICULAR_PLAN SCP1 ON E1.KEY_STUDENT_CURRICULAR_PLAN = SCP1.ID_INTERNAL
