	private static InfoGuide getGuide(){
		SuportePersistenteOJB sp = null;
		IGuide guide = null;
		try {
			sp = SuportePersistenteOJB.getInstance();
			sp.iniciarTransaccao();
			guide = sp.getIPersistentGuide().readByNumberAndYearAndVersion(new Integer(1), new Integer(2003), new Integer(1));
			assertNotNull(guide);
			
			sp.confirmarTransaccao();
			
		} catch(Exception e) {
			fail("Error Reading From DataBase !!" + e);
		}
		
		return Cloner.copyIGuide2InfoGuide(guide);
	}

